public class Divisa {
    private String divisaUno;
    private  String divisaDos;

    public Divisa(String divisaUno, String divisaDos) {
        this.divisaUno = divisaUno;
        this.divisaDos = divisaDos;
    }

    public String getDivisaUno() {
        return divisaUno;
    }

    public void setDivisaUno(String divisaUno) {
        this.divisaUno = divisaUno;
    }

    public String getDivisaDos() {
        return divisaDos;
    }

    public void setDivisaDos(String divisaDos) {
        this.divisaDos = divisaDos;
    }
}
